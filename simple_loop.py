#  Two simple loops showing how to iterate.

#  Iterate through each name in a list of names and print them out to the console.
names = ['Rachel Belle Pepperstaffere', 'Laura Canbury', 'Samuel Riplee']

for name in names:
    print(name)

#  Iterate through each number in a list of numbers and print them out to the console.

numbers = [0, 100, 2560, 5520, 10250]

for num in numbers:
    print(num)
